# burda a ve b fonksiyonun parametredir
# istediğimiz kadar parametre ekleyebiliriz

# toplama(a,b) , fonksiyonun imzası - signature

def toplama(a,b):
    sonuc = a + b
    print(sonuc)

c = 11
d = 20

toplama(c,d)
toplama(45,78)

def toplama2(x, y, z):
    sonuc = x + y + z
    print(sonuc)

toplama2(3,5,7)